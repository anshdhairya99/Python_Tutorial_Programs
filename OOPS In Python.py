# What is OOPS:-----
# In Python object-oriented Programming (OOPs) is 
# a programming paradigm that uses objects and 
# classes in programming. 
# It aims to implement real-world entities like inheritance, polymorphisms, 
# encapsulation, etc. in the programming. 
# The main concept of object-oriented Programming (OOPs) or oops concepts 
# in Python is to bind the data and the functions that work together as a 
# single unit so that no other part of the code can access this data.

